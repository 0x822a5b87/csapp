// 更简单是用递归来实现
ListNode *Merge(ListNode *head, ListNode *anotherHead)
{
    if (head == nullptr)
    {
        return anotherHead;
    }
    else if (anotherHead == nullptr)
    {
        return head;
    }

    ListNode *mergedHead = (head->m_nValue < anotherHead->m_nValue) ? head : anotherHead;
    ListNode *tail = mergedHead;
    while (head != nullptr && anotherHead != nullptr)
    {
        if (head->m_nValue < anotherHead->m_nValue)
        {
            ListNode *nextHead = head->m_pNext;
            tail->m_pNext = head;
            tail = tail->m_pNext;
            head = nextHead;
        }
        else
        {
            ListNode *nextAnotherHead = anotherHead->m_pNext;
            tail->m_pNext = anotherHead;
            tail = tail->m_pNext;
            anotherHead = nextAnotherHead;
        }
    }

    ListNode *remainingNode = (head != nullptr) ? head : anotherHead;
    while (remainingNode != nullptr)
    {
        tail->m_pNext = remainingNode;
        remainingNode = remainingNode->m_pNext;
        tail = tail->m_pNext;
    }

    return mergedHead;
}

