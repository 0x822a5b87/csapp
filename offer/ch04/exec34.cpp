// 二叉树中和为某一值的路径
// 输入一颗二叉树和一个整数，打印出二叉树中节点值的和为输入整数的所有路径。

#include <vector>
#include "iostream"
#include "../Utilities/BinaryTree.hpp"

/*
 */
void DoFindPath(std::vector<BinaryTreeNode *> path, BinaryTreeNode *root, int expectedSum)
{
    if (root == nullptr)
    {
        return;
    }

    if (root->m_nValue == expectedSum)
    {
        std::cout << "path : ";
        std::for_each(path.cbegin(), path.cend(), [](BinaryTreeNode *node)
        {
            std::cout << node->m_nValue << " ";
        });

        std::cout << root->m_nValue << std::endl;
    }
    else if (root->m_nValue < expectedSum)
    {
        path.push_back(root);
        DoFindPath(path, root->m_pLeft, expectedSum - root->m_nValue);
        DoFindPath(path, root->m_pRight, expectedSum - root->m_nValue);
    }
}

void FindPath(BinaryTreeNode *root, int expectedSum)
{
    if (root == nullptr)
    {
        return;
    }

    std::vector<BinaryTreeNode *> leftPath, rightPath;
    leftPath.push_back(root);
    rightPath.push_back(root);
    DoFindPath(leftPath, root->m_pLeft, expectedSum - root->m_nValue);
    DoFindPath(rightPath, root->m_pRight, expectedSum - root->m_nValue);
    FindPath(root->m_pLeft, expectedSum);
    FindPath(root->m_pRight, expectedSum);
}

// ==================== 测试代码 ====================
void Test(char* testName, BinaryTreeNode* pRoot, int expectedSum)
{
    if(testName != nullptr)
        printf("%s begins:\n", testName);

    FindPath(pRoot, expectedSum);

    printf("\n");
}

